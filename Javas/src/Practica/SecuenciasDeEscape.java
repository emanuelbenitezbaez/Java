package Practica;

public class SecuenciasDeEscape {
    //Secuencias de escape
    public static void main(String[] args) {
        System.out.println("Hola mundo");
        System.out.println("Hola \n mundo");//Salto de linea
        System.out.println("Hola \t mundo");//Tabulador
        System.out.println("Hola \b mundo");//Retroceso
        System.out.println("Hola \r mundo");//Retorno de carro
        System.out.println("Hola \f mundo");//Salto de pagina
        System.out.println("Hola \\ mundo");//Barra invertida
        System.out.println("Hola \" mundo");//Comillas dobles
    }
}
